package com.github.appreciated.apexcharts.config.chart.toolbar;

public class Tools {
    private String download;
    private String selection;
    private String zoom;
    private String zoomin;
    private String zoomout;
    private String pan;
    private String reset;


    public Tools() {
    }

    public String getDownload() {
        return download;
    }

    public String getSelection() {
        return selection;
    }

    public String getZoom() {
        return zoom;
    }

    public String getZoomin() {
        return zoomin;
    }

    public String getZoomout() {
        return zoomout;
    }

    public String getPan() {
        return pan;
    }

    public String getReset() {
        return reset;
    }

    public void setDownload(String download) {
        this.download = download;
    }

    public void setSelection(String selection) {
        this.selection = selection;
    }

    public void setZoom(String zoom) {
        this.zoom = zoom;
    }

    public void setZoomin(String zoomin) {
        this.zoomin = zoomin;
    }

    public void setZoomout(String zoomout) {
        this.zoomout = zoomout;
    }

    public void setPan(String pan) {
        this.pan = pan;
    }

    public void setReset(String reset) {
        this.reset = reset;
    }

}
