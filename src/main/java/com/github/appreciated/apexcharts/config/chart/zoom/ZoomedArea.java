package com.github.appreciated.apexcharts.config.chart.zoom;

public class ZoomedArea {
    private Fill fill;
    private Stroke stroke;


    public ZoomedArea() {
    }

    public Fill getFill() {
        return fill;
    }

    public Stroke getStroke() {
        return stroke;
    }

    public void setFill(Fill fill) {
        this.fill = fill;
    }

    public void setStroke(Stroke stroke) {
        this.stroke = stroke;
    }

}
